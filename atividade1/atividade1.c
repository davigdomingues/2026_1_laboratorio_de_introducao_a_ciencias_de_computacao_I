#include <stdio.h>

int main() {
    int n; // variável para armazenar o número de vezes que a mensagem será exibida

    scanf("%d", &n); // lê o número de vezes que a mensagem deve ser exibida

    printf("Hello, World! %d\n", n); // exibe a mensagem "Hello, World!" seguida do número declarado pelo usuário

    return 0;
}